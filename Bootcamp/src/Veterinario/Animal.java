package Veterinario;

public class Animal {

	
		private String nombre;
	    private double peso;

	    // Constructor
	    public Animal(String nombre, double peso) {
	        this.nombre = nombre;
	        this.peso = peso;
	    }

	    // Getter para el atributo nombre
	    public String getNombre() {
	        return nombre;
	    }

	    // Getter para el atributo peso
	    public double getPeso() {
	        return peso;
	    }
	}


